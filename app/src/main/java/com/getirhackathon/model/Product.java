public class Product {
    private int _id;
    private String name;
    private String price;
    private String imgUrl;
    private String weight;

    public int getId() {
        return _id;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public String getWeight() {
        return weight;
    }
}
